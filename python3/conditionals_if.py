x = 1
y = 2

# To check if 2 values are equal use == operator
if x == 1:
    print('x equals 1!')

# To check if theyre not equal use != operator
if x != y:
    print('x does not equal y')

# To check if all conditions are true, use `and` keyword
if x == 1 and y < 3:
    print('both conditions are satisfied')

# To check if at least 1 condition is true, use `or` keyword
if x == 1 or y == 10000:
    print('one of the conditions what true!')


''' output
x equals 1!
x does not equal y
both conditions are satisfied
one of the conditions what true!
'''
